<template>
  <label class="c-settings-input c-settings-dropdown" v-click-outside="close">
    <span @click="toggle" class="label">
      <slot>{{ label }}</slot>
    </span>
    <span @click="open" class="input-text dropdown-label"> {{ selections[value] || empty }} </span>
    <ul class="c-settings-dropdown-body" v-if="opened" ref="dropdownlist">
      <li
        v-if="empty"
        @click="select('')"
        :class="{ active: value === '' }">
        {{ empty }}
      </li>
      <li
        v-for="(s, id) in selections"
        @click="select(id)"
        :class="{ active: value === id }">
        {{ s }}
      </li>
    </ul>
  </label>
</template>

<script>

export default {
  props: {
    label: { type: String },
    empty: { type: String },
    selections: { type: Object, required: true },
    value: { type: [ String, Number, Boolean ] }
  },
  data: () => ({
    opened: false
  }),
  methods: {
    open() {
      this.opened = true
      this.$nextTick(() => this.$refs.dropdownlist?.scrollIntoView({
        behavior: 'smooth',
        block: 'nearest'
      }))
    },
    select(opt) {
      this.$emit('input', opt)
      this.opened = false
    },
    close() {
      this.opened = false
    },
    toggle() {
      this.opened = !this.opened
    }
  },
  mounted() {
    // auto conversion from Boolean, to Number/String
    if(typeof this.value === 'boolean') {
      const n = this.value + 0
      // try Number first
      if('0' in this.selections && '1' in this.selections) {
        this.select(n + '')
        debugger
      }
      // String (true/false) not used currently
    }
  }
}

</script>

